const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;
const ServiceError = @import("errors.zig").ServiceError;

pub const GetAgentCardInput = struct {
    /// The ARN of the AgentCore Runtime agent for which you want to get the A2A
    /// agent card.
    agent_runtime_arn: []const u8,

    /// Optional qualifier to specify an agent alias, such as `prod`code> or `dev`.
    /// If you don't provide a value, the DEFAULT alias is used.
    qualifier: ?[]const u8 = null,

    /// The session ID that the AgentCore Runtime agent is using.
    runtime_session_id: ?[]const u8 = null,

    pub const json_field_names = .{
        .agent_runtime_arn = "agentRuntimeArn",
        .qualifier = "qualifier",
        .runtime_session_id = "runtimeSessionId",
    };
};

pub const GetAgentCardOutput = struct {
    /// An agent card document that contains metadata and capabilities for an
    /// AgentCore Runtime agent.
    agent_card: []const u8,

    /// The ID of the session associated with the AgentCore Runtime agent.
    runtime_session_id: ?[]const u8 = null,

    /// The status code of the request.
    status_code: ?i32 = null,

    pub const json_field_names = .{
        .agent_card = "agentCard",
        .runtime_session_id = "runtimeSessionId",
        .status_code = "statusCode",
    };
};

pub const Options = struct {
    diagnostic: ?*ServiceError = null,
};

pub fn execute(client: *Client, allocator: std.mem.Allocator, input: GetAgentCardInput, options: Options) !GetAgentCardOutput {
    var arena = std.heap.ArenaAllocator.init(client.allocator);
    defer arena.deinit();
    const alloc = arena.allocator();

    var request = try serializeRequest(alloc, input, client.config);
    defer request.deinit(alloc);

    const creds = try client.config.credentials.getCredentials(alloc);
    try aws.signing.signRequest(alloc, &request, creds, client.config.region, "bedrockagentcore");

    var response = try client.http_client.sendRequest(&request);
    defer response.deinit();

    if (!response.isSuccess()) {
        if (options.diagnostic) |d| {
            d.* = parseErrorResponse(response.body, response.status, client.allocator) catch .{ .kind = .{ .unknown = .{ .http_status = @intCast(response.status) } } };
        }
        return error.ServiceError;
    }

    const result = try deserializeResponse(response.body, response.status, response.headers, allocator);
    return result;
}

fn serializeRequest(alloc: std.mem.Allocator, input: GetAgentCardInput, config: *aws.Config) !aws.http.Request {
    const endpoint = try config.getEndpointForService("bedrockagentcore", "Bedrock AgentCore", alloc);

    const host = aws.url.parseHost(endpoint);
    const tls = !std.mem.startsWith(u8, endpoint, "http://");
    const port = aws.url.parsePort(endpoint);

    var path_buf: std.ArrayList(u8) = .{};
    try path_buf.appendSlice(alloc, "/runtimes/");
    try path_buf.appendSlice(alloc, input.agent_runtime_arn);
    try path_buf.appendSlice(alloc, "/invocations/.well-known/agent-card.json");
    const path = try path_buf.toOwnedSlice(alloc);

    var query_buf: std.ArrayList(u8) = .{};
    var query_has_prev = false;
    if (input.qualifier) |v| {
        if (query_has_prev) try query_buf.appendSlice(alloc, "&");
        try query_buf.appendSlice(alloc, "qualifier=");
        try aws.url.appendUrlEncoded(alloc, &query_buf, v);
        query_has_prev = true;
    }
    const query = try query_buf.toOwnedSlice(alloc);

    const body: ?[]const u8 = null;

    var request = aws.http.Request.init(host);
    request.method = .GET;
    request.path = path;
    request.tls = tls;
    request.port = port;
    request.body = body;
    request.query = query;
    try request.headers.put(alloc, "Content-Type", "application/json");
    if (input.runtime_session_id) |v| {
        try request.headers.put(alloc, "X-Amzn-Bedrock-AgentCore-Runtime-Session-Id", v);
    }

    return request;
}

fn deserializeResponse(body: []const u8, status: u16, headers: anytype, alloc: std.mem.Allocator) !GetAgentCardOutput {
    var result: GetAgentCardOutput = .{};
    result.agent_card = try alloc.dupe(u8, body);
    result.status_code = @intCast(status);
    if (headers.get("x-amzn-bedrock-agentcore-runtime-session-id")) |value| {
        result.runtime_session_id = try alloc.dupe(u8, value);
    }

    return result;
}

