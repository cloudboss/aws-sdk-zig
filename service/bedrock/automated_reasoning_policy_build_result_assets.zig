const AutomatedReasoningPolicyBuildLog = @import("automated_reasoning_policy_build_log.zig").AutomatedReasoningPolicyBuildLog;
const AutomatedReasoningPolicyGeneratedTestCases = @import("automated_reasoning_policy_generated_test_cases.zig").AutomatedReasoningPolicyGeneratedTestCases;
const AutomatedReasoningPolicyDefinition = @import("automated_reasoning_policy_definition.zig").AutomatedReasoningPolicyDefinition;
const AutomatedReasoningPolicyScenarios = @import("automated_reasoning_policy_scenarios.zig").AutomatedReasoningPolicyScenarios;
const AutomatedReasoningPolicyDefinitionQualityReport = @import("automated_reasoning_policy_definition_quality_report.zig").AutomatedReasoningPolicyDefinitionQualityReport;

/// Contains the various assets generated during a policy build workflow,
/// including logs, quality reports, test cases, and the final policy
/// definition.
pub const AutomatedReasoningPolicyBuildResultAssets = union(enum) {
    /// The complete build log containing detailed information about each step in
    /// the policy generation process.
    build_log: ?AutomatedReasoningPolicyBuildLog,
    /// A comprehensive test suite generated by the build workflow, providing
    /// validation capabilities for automated reasoning policies.
    generated_test_cases: ?AutomatedReasoningPolicyGeneratedTestCases,
    /// The complete policy definition generated by the build workflow, containing
    /// all rules, variables, and custom types extracted from the source documents.
    policy_definition: ?AutomatedReasoningPolicyDefinition,
    /// An entity encompassing all the policy scenarios generated by the build
    /// workflow, which can be used to validate an Automated Reasoning policy.
    policy_scenarios: ?AutomatedReasoningPolicyScenarios,
    /// A comprehensive report analyzing the quality of the generated policy,
    /// including metrics about rule coverage, potential conflicts, and unused
    /// elements.
    quality_report: ?AutomatedReasoningPolicyDefinitionQualityReport,

    pub const json_field_names = .{
        .build_log = "buildLog",
        .generated_test_cases = "generatedTestCases",
        .policy_definition = "policyDefinition",
        .policy_scenarios = "policyScenarios",
        .quality_report = "qualityReport",
    };
};
