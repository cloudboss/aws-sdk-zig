const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;

const list_endpoints = @import("list_endpoints.zig");
const list_outposts_with_s3 = @import("list_outposts_with_s3.zig");
const list_shared_endpoints = @import("list_shared_endpoints.zig");

pub const ListEndpointsPaginator = struct {
    client: *Client,
    params: list_endpoints.ListEndpointsInput,
    next_token: ?[]const u8 = null,
    done: bool = false,
    allocator: std.mem.Allocator,

    const Self = @This();

    pub fn next(self: *Self, allocator: std.mem.Allocator, options: list_endpoints.Options) !list_endpoints.ListEndpointsOutput {
        if (self.done) {
            return error.EndOfPagination;
        }

        self.params.next_token = self.next_token;

        const output = try list_endpoints.execute(self.client, allocator, self.params, options);

        if (output.next_token) |token| {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = self.allocator.dupe(u8, token) catch null;
        } else {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = null;
            self.done = true;
        }

        return output;
    }

    pub fn deinit(self: *Self) void {
        if (self.next_token) |token| {
            self.allocator.free(token);
        }
    }
};

