const WebhookDefinition = @import("webhook_definition.zig").WebhookDefinition;
const Tag = @import("tag.zig").Tag;

/// The detail returned for each webhook after listing webhooks, such as the
/// webhook
/// URL, the webhook name, and the webhook ARN.
pub const ListWebhookItem = struct {
    /// The Amazon Resource Name (ARN) of the webhook.
    arn: ?[]const u8,

    /// The detail returned for each webhook, such as the webhook authentication
    /// type and
    /// filter rules.
    definition: WebhookDefinition,

    /// The number code of the error.
    error_code: ?[]const u8,

    /// The text of the error message about the webhook.
    error_message: ?[]const u8,

    /// The date and time a webhook was last successfully triggered, in timestamp
    /// format.
    last_triggered: ?i64,

    /// Specifies the tags applied to the webhook.
    tags: ?[]const Tag,

    /// A unique URL generated by CodePipeline. When a POST request is made to this
    /// URL, the defined pipeline is started as long as the body of the post request
    /// satisfies
    /// the defined authentication and filtering conditions. Deleting and
    /// re-creating a webhook
    /// makes the old URL invalid and generates a new one.
    url: []const u8,

    pub const json_field_names = .{
        .arn = "arn",
        .definition = "definition",
        .error_code = "errorCode",
        .error_message = "errorMessage",
        .last_triggered = "lastTriggered",
        .tags = "tags",
        .url = "url",
    };
};
