const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;

const list_challenge_metadata = @import("list_challenge_metadata.zig");
const list_connectors = @import("list_connectors.zig");

pub const ListChallengeMetadataPaginator = struct {
    client: *Client,
    params: list_challenge_metadata.ListChallengeMetadataInput,
    next_token: ?[]const u8 = null,
    done: bool = false,
    allocator: std.mem.Allocator,

    const Self = @This();

    pub fn next(self: *Self, allocator: std.mem.Allocator, options: list_challenge_metadata.Options) !list_challenge_metadata.ListChallengeMetadataOutput {
        if (self.done) {
            return error.EndOfPagination;
        }

        self.params.next_token = self.next_token;

        const output = try list_challenge_metadata.execute(self.client, allocator, self.params, options);

        if (output.next_token) |token| {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = self.allocator.dupe(u8, token) catch null;
        } else {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = null;
            self.done = true;
        }

        return output;
    }

    pub fn deinit(self: *Self) void {
        if (self.next_token) |token| {
            self.allocator.free(token);
        }
    }
};

