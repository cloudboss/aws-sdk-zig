const ChallengePreference = @import("challenge_preference.zig").ChallengePreference;
const LivenessOutputConfig = @import("liveness_output_config.zig").LivenessOutputConfig;

/// A session settings object. It contains settings for the operation to be
/// performed. It
/// accepts arguments for OutputConfig and AuditImagesLimit.
pub const CreateFaceLivenessSessionRequestSettings = struct {
    /// Number of audit images to be returned back. Takes an integer between 0-4.
    /// Any integer less
    /// than 0 will return 0, any integer above 4 will return 4 images in the
    /// response. By default, it
    /// is set to 0. The limit is best effort and is based on the actual duration of
    /// the
    /// selfie-video.
    audit_images_limit: ?i32,

    /// Indicates preferred challenge types and versions for the Face Liveness
    /// session to be created.
    challenge_preferences: ?[]const ChallengePreference,

    /// Can specify the location of an Amazon S3 bucket, where reference and audit
    /// images will be
    /// stored. Note that the Amazon S3 bucket must be located in the caller's AWS
    /// account and in the
    /// same region as the Face Liveness end-point. Additionally, the Amazon S3
    /// object keys are
    /// auto-generated by the Face Liveness system. Requires that the caller has the
    /// `s3:PutObject` permission on the Amazon S3 bucket.
    output_config: ?LivenessOutputConfig,

    pub const json_field_names = .{
        .audit_images_limit = "AuditImagesLimit",
        .challenge_preferences = "ChallengePreferences",
        .output_config = "OutputConfig",
    };
};
