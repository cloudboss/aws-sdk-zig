const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;
const ServiceError = @import("errors.zig").ServiceError;
const CreateOAuth2TokenRequestBody = @import("create_o_auth_2_token_request_body.zig").CreateOAuth2TokenRequestBody;
const CreateOAuth2TokenResponseBody = @import("create_o_auth_2_token_response_body.zig").CreateOAuth2TokenResponseBody;

pub const CreateOAuth2TokenInput = struct {
    /// Flattened token operation inputs
    /// The specific operation is determined by grant_type in the request body
    token_input: CreateOAuth2TokenRequestBody,

    pub const json_field_names = .{
        .token_input = "tokenInput",
    };
};

pub const CreateOAuth2TokenOutput = struct {
    /// Flattened token operation outputs
    /// The specific response fields depend on the grant_type used in the request
    token_output: ?CreateOAuth2TokenResponseBody = null,

    pub const json_field_names = .{
        .token_output = "tokenOutput",
    };
};

pub const Options = struct {
    diagnostic: ?*ServiceError = null,
};

pub fn execute(client: *Client, allocator: std.mem.Allocator, input: CreateOAuth2TokenInput, options: Options) !CreateOAuth2TokenOutput {
    var arena = std.heap.ArenaAllocator.init(client.allocator);
    defer arena.deinit();
    const alloc = arena.allocator();

    var request = try serializeRequest(alloc, input, client.config);
    defer request.deinit(alloc);

    const creds = try client.config.credentials.getCredentials(alloc);
    try aws.signing.signRequest(alloc, &request, creds, client.config.region, "signin");

    var response = try client.http_client.sendRequest(&request);
    defer response.deinit();

    if (!response.isSuccess()) {
        if (options.diagnostic) |d| {
            d.* = parseErrorResponse(response.body, response.status, client.allocator) catch .{ .kind = .{ .unknown = .{ .http_status = @intCast(response.status) } } };
        }
        return error.ServiceError;
    }

    const result = try deserializeResponse(response.body, response.status, response.headers, allocator);
    return result;
}

fn serializeRequest(alloc: std.mem.Allocator, input: CreateOAuth2TokenInput, config: *aws.Config) !aws.http.Request {
    const endpoint = try config.getEndpointForService("signin", "Signin", alloc);

    const host = aws.url.parseHost(endpoint);
    const tls = !std.mem.startsWith(u8, endpoint, "http://");
    const port = aws.url.parsePort(endpoint);

    const path = "/v1/token";

    const body = try aws.json.jsonStringify(input.token_input, alloc);

    var request = aws.http.Request.init(host);
    request.method = .POST;
    request.path = path;
    request.tls = tls;
    request.port = port;
    request.body = body;
    try request.headers.put(alloc, "Content-Type", "application/json");

    return request;
}

