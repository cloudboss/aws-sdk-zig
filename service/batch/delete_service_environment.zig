pub fn execute(client: *Client, allocator: std.mem.Allocator, input: DeleteServiceEnvironmentInput, options: Options) !DeleteServiceEnvironmentOutput {
    var arena = std.heap.ArenaAllocator.init(client.allocator);
    defer arena.deinit();
