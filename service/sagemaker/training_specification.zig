const AdditionalS3DataSource = @import("additional_s3_data_source.zig").AdditionalS3DataSource;
const MetricDefinition = @import("metric_definition.zig").MetricDefinition;
const HyperParameterSpecification = @import("hyper_parameter_specification.zig").HyperParameterSpecification;
const TrainingInstanceType = @import("training_instance_type.zig").TrainingInstanceType;
const HyperParameterTuningJobObjective = @import("hyper_parameter_tuning_job_objective.zig").HyperParameterTuningJobObjective;
const ChannelSpecification = @import("channel_specification.zig").ChannelSpecification;

/// Defines how the algorithm is used for a training job.
pub const TrainingSpecification = struct {
    /// The additional data source used during the training job.
    additional_s3_data_source: ?AdditionalS3DataSource,

    /// A list of `MetricDefinition` objects, which are used for parsing metrics
    /// generated by the algorithm.
    metric_definitions: ?[]const MetricDefinition,

    /// A list of the `HyperParameterSpecification` objects, that define the
    /// supported hyperparameters. This is required if the algorithm supports
    /// automatic model tuning.>
    supported_hyper_parameters: ?[]const HyperParameterSpecification,

    /// A list of the instance types that this algorithm can use for training.
    supported_training_instance_types: []const TrainingInstanceType,

    /// A list of the metrics that the algorithm emits that can be used as the
    /// objective metric in a hyperparameter tuning job.
    supported_tuning_job_objective_metrics: ?[]const HyperParameterTuningJobObjective,

    /// Indicates whether the algorithm supports distributed training. If set to
    /// false, buyers can't request more than one instance during training.
    supports_distributed_training: ?bool,

    /// A list of `ChannelSpecification` objects, which specify the input sources to
    /// be used by the algorithm.
    training_channels: []const ChannelSpecification,

    /// The Amazon ECR registry path of the Docker image that contains the training
    /// algorithm.
    training_image: []const u8,

    /// An MD5 hash of the training algorithm that identifies the Docker image used
    /// for training.
    training_image_digest: ?[]const u8,

    pub const json_field_names = .{
        .additional_s3_data_source = "AdditionalS3DataSource",
        .metric_definitions = "MetricDefinitions",
        .supported_hyper_parameters = "SupportedHyperParameters",
        .supported_training_instance_types = "SupportedTrainingInstanceTypes",
        .supported_tuning_job_objective_metrics = "SupportedTuningJobObjectiveMetrics",
        .supports_distributed_training = "SupportsDistributedTraining",
        .training_channels = "TrainingChannels",
        .training_image = "TrainingImage",
        .training_image_digest = "TrainingImageDigest",
    };
};
