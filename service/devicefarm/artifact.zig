const ArtifactType = @import("artifact_type.zig").ArtifactType;

/// Represents the output of a test. Examples of artifacts include logs and
/// screenshots.
pub const Artifact = struct {
    /// The artifact's ARN.
    arn: ?[]const u8,

    /// The artifact's file extension.
    extension: ?[]const u8,

    /// The artifact's name.
    name: ?[]const u8,

    /// The artifact's type.
    ///
    /// Allowed values include the following:
    ///
    /// * UNKNOWN
    ///
    /// * SCREENSHOT
    ///
    /// * DEVICE_LOG
    ///
    /// * MESSAGE_LOG
    ///
    /// * VIDEO_LOG
    ///
    /// * RESULT_LOG
    ///
    /// * SERVICE_LOG
    ///
    /// * WEBKIT_LOG
    ///
    /// * INSTRUMENTATION_OUTPUT
    ///
    /// * EXERCISER_MONKEY_OUTPUT: the artifact (log) generated by an Android fuzz
    ///   test.
    ///
    /// * APPIUM_SERVER_OUTPUT
    ///
    /// * APPIUM_JAVA_OUTPUT
    ///
    /// * APPIUM_JAVA_XML_OUTPUT
    ///
    /// * APPIUM_PYTHON_OUTPUT
    ///
    /// * APPIUM_PYTHON_XML_OUTPUT
    ///
    /// * APPLICATION_CRASH_REPORT
    ///
    /// * XCTEST_LOG
    ///
    /// * VIDEO
    ///
    /// * CUSTOMER_ARTIFACT
    ///
    /// * CUSTOMER_ARTIFACT_LOG
    ///
    /// * TESTSPEC_OUTPUT
    @"type": ?ArtifactType,

    /// The presigned Amazon S3 URL that can be used with a GET request to download
    /// the artifact's
    /// file.
    url: ?[]const u8,

    pub const json_field_names = .{
        .arn = "arn",
        .extension = "extension",
        .name = "name",
        .@"type" = "type",
        .url = "url",
    };
};
