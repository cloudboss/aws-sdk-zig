const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;

const list_instance_types = @import("list_instance_types.zig");
const list_regions = @import("list_regions.zig");
const list_workspace_instances = @import("list_workspace_instances.zig");

pub const ListInstanceTypesPaginator = struct {
    client: *Client,
    params: list_instance_types.ListInstanceTypesInput,
    next_token: ?[]const u8 = null,
    done: bool = false,
    allocator: std.mem.Allocator,

    const Self = @This();

    pub fn next(self: *Self, allocator: std.mem.Allocator, options: list_instance_types.Options) !list_instance_types.ListInstanceTypesOutput {
        if (self.done) {
            return error.EndOfPagination;
        }

        self.params.next_token = self.next_token;

        const output = try list_instance_types.execute(self.client, allocator, self.params, options);

        if (output.next_token) |token| {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = self.allocator.dupe(u8, token) catch null;
        } else {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = null;
            self.done = true;
        }

        return output;
    }

    pub fn deinit(self: *Self) void {
        if (self.next_token) |token| {
            self.allocator.free(token);
        }
    }
};

