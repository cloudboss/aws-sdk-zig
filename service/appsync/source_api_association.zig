const SourceApiAssociationConfig = @import("source_api_association_config.zig").SourceApiAssociationConfig;
const SourceApiAssociationStatus = @import("source_api_association_status.zig").SourceApiAssociationStatus;

/// Describes the configuration of a source API. A source API is a GraphQL API
/// that is
/// linked to a merged API. There can be multiple source APIs attached to each
/// merged API. When
/// linked to a merged API, the source API's schema, data sources, and resolvers
/// will be
/// combined with other linked source API data to form a new, singular API.
///
/// Source APIs can originate from your account or from other accounts via
/// Amazon Web Services Resource Access Manager. For more information about
/// sharing resources from other
/// accounts, see [What is Amazon Web Services Resource Access
/// Manager?](https://docs.aws.amazon.com/ram/latest/userguide/what-is.html) in
/// the *Amazon Web Services Resource Access Manager* guide.
pub const SourceApiAssociation = struct {
    /// The Amazon Resource Name (ARN) of the source API association.
    association_arn: ?[]const u8,

    /// The ID generated by the AppSync service for the source API association.
    association_id: ?[]const u8,

    /// The description field.
    description: ?[]const u8,

    /// The datetime value of the last successful merge of the source API
    /// association. The
    /// result will be in UTC format and your local time zone.
    last_successful_merge_date: ?i64,

    /// The Amazon Resource Name (ARN) of the AppSync Merged API.
    merged_api_arn: ?[]const u8,

    /// The ID of the AppSync Merged API.
    merged_api_id: ?[]const u8,

    /// The Amazon Resource Name (ARN) of the AppSync source API.
    source_api_arn: ?[]const u8,

    /// The `SourceApiAssociationConfig` object data.
    source_api_association_config: ?SourceApiAssociationConfig,

    /// The state of the source API association.
    source_api_association_status: ?SourceApiAssociationStatus,

    /// The detailed message related to the current state of the source API
    /// association.
    source_api_association_status_detail: ?[]const u8,

    /// The ID of the AppSync source API.
    source_api_id: ?[]const u8,

    pub const json_field_names = .{
        .association_arn = "associationArn",
        .association_id = "associationId",
        .description = "description",
        .last_successful_merge_date = "lastSuccessfulMergeDate",
        .merged_api_arn = "mergedApiArn",
        .merged_api_id = "mergedApiId",
        .source_api_arn = "sourceApiArn",
        .source_api_association_config = "sourceApiAssociationConfig",
        .source_api_association_status = "sourceApiAssociationStatus",
        .source_api_association_status_detail = "sourceApiAssociationStatusDetail",
        .source_api_id = "sourceApiId",
    };
};
