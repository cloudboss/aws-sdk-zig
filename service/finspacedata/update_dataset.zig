fn serializeRequest(alloc: std.mem.Allocator, input: UpdateDatasetInput, config: *aws.Config) !aws.http.Request {
    const endpoint = try config.getEndpointForService("finspacedata", "finspace data", alloc);

    const host = aws.url.parseHost(endpoint);
    const tls = !std.mem.startsWith(u8, endpoint, "http://");
    const port = aws.url.parsePort(endpoint);

    var path_buf: std.ArrayList(u8) = .{};
    try path_buf.appendSlice(alloc, "/datasetsv2/");
    try path_buf.appendSlice(alloc, input.dataset_id);
    const path = try path_buf.toOwnedSlice(alloc);

