const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;

const get_free_tier_usage = @import("get_free_tier_usage.zig");
const list_account_activities = @import("list_account_activities.zig");

pub const GetFreeTierUsagePaginator = struct {
    client: *Client,
    params: get_free_tier_usage.GetFreeTierUsageInput,
    next_token: ?[]const u8 = null,
    done: bool = false,
    allocator: std.mem.Allocator,

    const Self = @This();

    pub fn next(self: *Self, allocator: std.mem.Allocator, options: get_free_tier_usage.Options) !get_free_tier_usage.GetFreeTierUsageOutput {
        if (self.done) {
            return error.EndOfPagination;
        }

        self.params.next_token = self.next_token;

        const output = try get_free_tier_usage.execute(self.client, allocator, self.params, options);

        if (output.next_token) |token| {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = self.allocator.dupe(u8, token) catch null;
        } else {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = null;
            self.done = true;
        }

        return output;
    }

    pub fn deinit(self: *Self) void {
        if (self.next_token) |token| {
            self.allocator.free(token);
        }
    }
};

