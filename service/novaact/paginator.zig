const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;

const list_acts = @import("list_acts.zig");
const list_sessions = @import("list_sessions.zig");
const list_workflow_definitions = @import("list_workflow_definitions.zig");
const list_workflow_runs = @import("list_workflow_runs.zig");

pub const ListActsPaginator = struct {
    client: *Client,
    params: list_acts.ListActsInput,
    next_token: ?[]const u8 = null,
    done: bool = false,
    allocator: std.mem.Allocator,

    const Self = @This();

    pub fn next(self: *Self, allocator: std.mem.Allocator, options: list_acts.Options) !list_acts.ListActsOutput {
        if (self.done) {
            return error.EndOfPagination;
        }

        self.params.next_token = self.next_token;

        const output = try list_acts.execute(self.client, allocator, self.params, options);

        if (output.next_token) |token| {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = self.allocator.dupe(u8, token) catch null;
        } else {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = null;
            self.done = true;
        }

        return output;
    }

    pub fn deinit(self: *Self) void {
        if (self.next_token) |token| {
            self.allocator.free(token);
        }
    }
};

