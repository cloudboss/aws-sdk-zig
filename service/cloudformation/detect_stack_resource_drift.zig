const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;
const ServiceError = @import("errors.zig").ServiceError;
const StackResourceDrift = @import("stack_resource_drift.zig").StackResourceDrift;
const serde = @import("serde.zig");

pub const DetectStackResourceDriftInput = struct {
    /// The logical name of the resource for which to return drift information.
    logical_resource_id: []const u8,

    /// The name of the stack to which the resource belongs.
    stack_name: []const u8,
};

pub const DetectStackResourceDriftOutput = struct {
    /// Information about whether the resource's actual configuration has drifted
    /// from its
    /// expected template configuration, including actual and expected property
    /// values and any
    /// differences detected.
    stack_resource_drift: ?StackResourceDrift = null,
};

pub const Options = struct {
    diagnostic: ?*ServiceError = null,
};

pub fn execute(client: *Client, allocator: std.mem.Allocator, input: DetectStackResourceDriftInput, options: Options) !DetectStackResourceDriftOutput {
    var arena = std.heap.ArenaAllocator.init(client.allocator);
    defer arena.deinit();
    const alloc = arena.allocator();

    var request = try serializeRequest(alloc, input, client.config);
    defer request.deinit(alloc);

    const creds = try client.config.credentials.getCredentials(alloc);
    try aws.signing.signRequest(alloc, &request, creds, client.config.region, "cloudformation");

    var response = try client.http_client.sendRequest(&request);
    defer response.deinit();

    if (!response.isSuccess()) {
        if (options.diagnostic) |d| {
            d.* = parseErrorResponse(response.body, response.status, client.allocator) catch .{ .kind = .{ .unknown = .{ .http_status = @intCast(response.status) } } };
        }
        return error.ServiceError;
    }

    const result = try deserializeResponse(response.body, response.status, response.headers, allocator);
    return result;
}

fn serializeRequest(alloc: std.mem.Allocator, input: DetectStackResourceDriftInput, config: *aws.Config) !aws.http.Request {
    const endpoint = try config.getEndpointForService("cloudformation", "CloudFormation", alloc);

    const host = aws.url.parseHost(endpoint);
    const tls = !std.mem.startsWith(u8, endpoint, "http://");
    const port = aws.url.parsePort(endpoint);

    var body_buf: std.ArrayList(u8) = .{};

    try body_buf.appendSlice(alloc, "Action=DetectStackResourceDrift&Version=2010-05-15");
    try body_buf.appendSlice(alloc, "&LogicalResourceId=");
    try aws.url.appendUrlEncoded(alloc, &body_buf, input.logical_resource_id);
    try body_buf.appendSlice(alloc, "&StackName=");
    try aws.url.appendUrlEncoded(alloc, &body_buf, input.stack_name);

    const body = try body_buf.toOwnedSlice(alloc);

    var request = aws.http.Request.init(host);
    request.method = .POST;
    request.path = "/";
    request.tls = tls;
    request.port = port;
    request.body = body;
    try request.headers.put(alloc, "Content-Type", "application/x-www-form-urlencoded");

    return request;
}

fn deserializeResponse(body: []const u8, status: u16, headers: anytype, alloc: std.mem.Allocator) !DetectStackResourceDriftOutput {
    _ = status;
    _ = headers;
    var reader = aws.xml.Reader.init(body);

    while (try reader.next()) |event| {
        switch (event) {
            .element_start => |e| {
                if (std.mem.eql(u8, e.local, "DetectStackResourceDriftResult")) break;
            },
            else => {},
        }
    }

    var result: DetectStackResourceDriftOutput = .{};
    while (try reader.next()) |event| {
        switch (event) {
            .element_start => |e| {
                if (std.mem.eql(u8, e.local, "StackResourceDrift")) {
                    result.stack_resource_drift = try serde.deserializeStackResourceDrift(&reader, alloc);
                } else {
                    try reader.skipElement();
                }
            },
            .element_end => break,
            else => {},
        }
    }

    return result;
}

