const DetailedStatus = @import("detailed_status.zig").DetailedStatus;
const HookFailureMode = @import("hook_failure_mode.zig").HookFailureMode;
const HookInvocationPoint = @import("hook_invocation_point.zig").HookInvocationPoint;
const HookStatus = @import("hook_status.zig").HookStatus;
const ResourceStatus = @import("resource_status.zig").ResourceStatus;

/// The `StackEvent` data type.
pub const StackEvent = struct {
    /// The token passed to the operation that generated this event.
    ///
    /// All events triggered by a given stack operation are assigned the same client
    /// request token,
    /// which you can use to track operations. For example, if you execute a
    /// `CreateStack`
    /// operation with the token `token1`, then all the `StackEvents` generated by
    /// that operation will have `ClientRequestToken` set as `token1`.
    ///
    /// In the console, stack operations display the client request token on the
    /// Events tab. Stack
    /// operations that are initiated from the console use the token format
    /// *Console-StackOperation-ID*, which helps you easily identify the stack
    /// operation . For example, if you create a stack using the console, each stack
    /// event would be
    /// assigned the same token in the following format:
    /// `Console-CreateStack-7f59c3cf-00d2-40c7-b2ff-e75db0987002`.
    client_request_token: ?[]const u8 = null,

    /// An optional field that contains information about the detailed status of the
    /// stack
    /// event.
    ///
    /// * `CONFIGURATION_COMPLETE` - all of the resources in the stack have reached
    ///   that
    /// event. For more information, see [Understand
    /// CloudFormation stack creation
    /// events](https://docs.aws.amazon.com/AWSCloudFormation/latest/UserGuide/stack-resource-configuration-complete.html) in the *CloudFormation User Guide*.
    ///
    /// * `VALIDATION_FAILED` - template validation failed because of invalid
    ///   properties
    /// in the template. The `ResourceStatusReason` field shows what properties are
    /// defined
    /// incorrectly.
    detailed_status: ?DetailedStatus = null,

    /// The unique identifier of this event.
    event_id: []const u8,

    /// Specify the Hook failure mode for non-compliant resources in the followings
    /// ways.
    ///
    /// * `FAIL` Stops provisioning resources.
    ///
    /// * `WARN` Allows provisioning to continue with a warning message.
    hook_failure_mode: ?HookFailureMode = null,

    /// The unique identifier of the Hook invocation.
    hook_invocation_id: ?[]const u8 = null,

    /// The specific point in the provisioning process where the Hook is invoked.
    hook_invocation_point: ?HookInvocationPoint = null,

    /// Provides the status of the change set Hook.
    hook_status: ?HookStatus = null,

    /// Provides the reason for the Hook status.
    hook_status_reason: ?[]const u8 = null,

    /// The name of the Hook.
    hook_type: ?[]const u8 = null,

    /// The logical name of the resource specified in the template.
    logical_resource_id: ?[]const u8 = null,

    /// The unique identifier of the operation that generated this stack event.
    operation_id: ?[]const u8 = null,

    /// The name or unique identifier associated with the physical instance of the
    /// resource.
    physical_resource_id: ?[]const u8 = null,

    /// BLOB of the properties used to create the resource.
    resource_properties: ?[]const u8 = null,

    /// Current status of the resource.
    resource_status: ?ResourceStatus = null,

    /// Success/failure message associated with the resource.
    resource_status_reason: ?[]const u8 = null,

    /// Type of resource. For more information, see [Amazon Web Services resource
    /// and
    /// property types
    /// reference](https://docs.aws.amazon.com/AWSCloudFormation/latest/UserGuide/aws-template-resource-type-ref.html) in the *CloudFormation User Guide*.
    resource_type: ?[]const u8 = null,

    /// The unique ID name of the instance of the stack.
    stack_id: []const u8,

    /// The name associated with a stack.
    stack_name: []const u8,

    /// Time the status was updated.
    timestamp: i64,
};
