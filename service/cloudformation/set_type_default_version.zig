fn serializeRequest(alloc: std.mem.Allocator, input: SetTypeDefaultVersionInput, config: *aws.Config) !aws.http.Request {
    const endpoint = try config.getEndpointForService("cloudformation", "CloudFormation", alloc);

    const host = aws.url.parseHost(endpoint);
    const tls = !std.mem.startsWith(u8, endpoint, "http://");
    const port = aws.url.parsePort(endpoint);

    var body_buf: std.ArrayList(u8) = .{};

    try body_buf.appendSlice(alloc, "Action=SetTypeDefaultVersion&Version=2010-05-15");
    if (input.arn) |v| {
        try body_buf.appendSlice(alloc, "&Arn=");
        try aws.url.appendUrlEncoded(alloc, &body_buf, v);
    }
    if (input.@"type") |v| {
        try body_buf.appendSlice(alloc, "&Type=");
        try aws.url.appendUrlEncoded(alloc, &body_buf, @tagName(v));
    }
    if (input.type_name) |v| {
        try body_buf.appendSlice(alloc, "&TypeName=");
        try aws.url.appendUrlEncoded(alloc, &body_buf, v);
    }
    if (input.version_id) |v| {
        try body_buf.appendSlice(alloc, "&VersionId=");
        try aws.url.appendUrlEncoded(alloc, &body_buf, v);
    }

    const body = try body_buf.toOwnedSlice(alloc);

    var request = aws.http.Request.init(host);
    request.method = .POST;
    request.path = "/";
    request.tls = tls;
    request.port = port;
    request.body = body;
    try request.headers.put(alloc, "Content-Type", "application/x-www-form-urlencoded");

    return request;
}

