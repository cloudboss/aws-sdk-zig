    has_prev = true;
    if (has_prev) try body_buf.appendSlice(allocator, ",");
    try body_buf.appendSlice(allocator, "\"ExportDestination\":");
    try aws.json.writeValue(@TypeOf(input.export_destination), input.export_destination, allocator, &body_buf);
