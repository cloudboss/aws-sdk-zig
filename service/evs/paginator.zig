const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;

const list_environment_hosts = @import("list_environment_hosts.zig");
const list_environment_vlans = @import("list_environment_vlans.zig");
const list_environments = @import("list_environments.zig");

pub const ListEnvironmentHostsPaginator = struct {
    client: *Client,
    params: list_environment_hosts.ListEnvironmentHostsInput,
    next_token: ?[]const u8 = null,
    done: bool = false,
    allocator: std.mem.Allocator,

    const Self = @This();

    pub fn next(self: *Self, allocator: std.mem.Allocator, options: list_environment_hosts.Options) !list_environment_hosts.ListEnvironmentHostsOutput {
        if (self.done) {
            return error.EndOfPagination;
        }

        self.params.next_token = self.next_token;

        const output = try list_environment_hosts.execute(self.client, allocator, self.params, options);

        if (output.next_token) |token| {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = self.allocator.dupe(u8, token) catch null;
        } else {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = null;
            self.done = true;
        }

        return output;
    }

    pub fn deinit(self: *Self) void {
        if (self.next_token) |token| {
            self.allocator.free(token);
        }
    }
};

