    /// The ID of the job to be canceled.
    job_id: []const u8,

    /// A collection of name/value pairs that describe the status of the job
    /// execution. If
    /// not specified, the statusDetails are unchanged. You can specify at most 10
    /// name/value
    /// pairs.
