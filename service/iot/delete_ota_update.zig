pub fn execute(client: *Client, allocator: std.mem.Allocator, input: DeleteOTAUpdateInput, options: Options) !DeleteOTAUpdateOutput {
    var arena = std.heap.ArenaAllocator.init(client.allocator);
    defer arena.deinit();
