const CertificateMode = @import("certificate_mode.zig").CertificateMode;
const CertificateStatus = @import("certificate_status.zig").CertificateStatus;
const TransferData = @import("transfer_data.zig").TransferData;
const CertificateValidity = @import("certificate_validity.zig").CertificateValidity;

/// Describes a certificate.
pub const CertificateDescription = struct {
    /// The certificate ID of the CA certificate used to sign this certificate.
    ca_certificate_id: ?[]const u8 = null,

    /// The ARN of the certificate.
    certificate_arn: ?[]const u8 = null,

    /// The ID of the certificate.
    certificate_id: ?[]const u8 = null,

    /// The mode of the certificate.
    ///
    /// `DEFAULT`: A certificate in `DEFAULT` mode is either generated by
    /// Amazon Web Services IoT Core or registered with an issuer certificate
    /// authority (CA) in `DEFAULT` mode. Devices with
    /// certificates in `DEFAULT` mode aren't required to send the Server Name
    /// Indication (SNI) extension when connecting to Amazon Web Services IoT Core.
    /// However, to use features such as
    /// custom domains and VPC endpoints, we recommend that you use the SNI
    /// extension when
    /// connecting to Amazon Web Services IoT Core.
    ///
    /// `SNI_ONLY`: A certificate in `SNI_ONLY` mode is registered without an issuer
    /// CA. Devices with certificates
    /// in `SNI_ONLY` mode must send the SNI extension when connecting to Amazon Web
    /// Services IoT Core.
    ///
    /// For more information about the value for SNI extension, see [Transport
    /// security in
    /// IoT](https://docs.aws.amazon.com/iot/latest/developerguide/transport-security.html).
    certificate_mode: ?CertificateMode = null,

    /// The certificate data, in PEM format.
    certificate_pem: ?[]const u8 = null,

    /// The date and time the certificate was created.
    creation_date: ?i64 = null,

    /// The customer version of the certificate.
    customer_version: ?i32 = null,

    /// The generation ID of the certificate.
    generation_id: ?[]const u8 = null,

    /// The date and time the certificate was last modified.
    last_modified_date: ?i64 = null,

    /// The ID of the Amazon Web Services account that owns the certificate.
    owned_by: ?[]const u8 = null,

    /// The ID of the Amazon Web Services account of the previous owner of the
    /// certificate.
    previous_owned_by: ?[]const u8 = null,

    /// The status of the certificate.
    status: ?CertificateStatus = null,

    /// The transfer data.
    transfer_data: ?TransferData = null,

    /// When the certificate is valid.
    validity: ?CertificateValidity = null,

    pub const json_field_names = .{
        .ca_certificate_id = "caCertificateId",
        .certificate_arn = "certificateArn",
        .certificate_id = "certificateId",
        .certificate_mode = "certificateMode",
        .certificate_pem = "certificatePem",
        .creation_date = "creationDate",
        .customer_version = "customerVersion",
        .generation_id = "generationId",
        .last_modified_date = "lastModifiedDate",
        .owned_by = "ownedBy",
        .previous_owned_by = "previousOwnedBy",
        .status = "status",
        .transfer_data = "transferData",
        .validity = "validity",
    };
};
