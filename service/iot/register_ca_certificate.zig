    try body_buf.appendSlice(alloc, "{");

    if (has_prev) try body_buf.appendSlice(alloc, ",");
    try body_buf.appendSlice(alloc, "\"caCertificate\":");
    try aws.json.writeValue(@TypeOf(input.ca_certificate), input.ca_certificate, alloc, &body_buf);
    has_prev = true;
    if (input.certificate_mode) |v| {
        if (has_prev) try body_buf.appendSlice(alloc, ",");
        try body_buf.appendSlice(alloc, "\"certificateMode\":");
        try aws.json.writeValue(@TypeOf(v), v, alloc, &body_buf);
