const aws = @import("aws");

const DestinationConfiguration = @import("destination_configuration.zig").DestinationConfiguration;
const LoggingConfigurationState = @import("logging_configuration_state.zig").LoggingConfigurationState;

/// Summary information about a logging configuration.
pub const LoggingConfigurationSummary = struct {
    /// Logging-configuration ARN.
    arn: ?[]const u8 = null,

    /// Time when the logging configuration was created. This is an ISO 8601
    /// timestamp;
    /// *note that this is returned as a string*.
    create_time: ?i64 = null,

    /// A complex type that contains a destination configuration for where chat
    /// content will be
    /// logged.
    destination_configuration: ?DestinationConfiguration = null,

    /// Logging-configuration ID, generated by the system. This is a relative
    /// identifier, the part
    /// of the ARN that uniquely identifies the room.
    id: ?[]const u8 = null,

    /// Logging-configuration name. The value does not need to be unique.
    name: ?[]const u8 = null,

    /// The state of the logging configuration. When this is `ACTIVE`, the
    /// configuration is ready for logging chat content.
    state: ?LoggingConfigurationState = null,

    /// Tags to attach to the resource. Array of maps, each of the form
    /// `string:string
    /// (key:value)`. See [Best practices and
    /// strategies](https://docs.aws.amazon.com/tag-editor/latest/userguide/best-practices-and-strats.html) in *Tagging Amazon Web Services Resources and Tag Editor* for details, including restrictions that apply to tags and "Tag naming limits and requirements"; Amazon IVS Chat has no constraints on tags beyond what is documented
    /// there.
    tags: ?[]const aws.map.StringMapEntry = null,

    /// Time of the logging configuration’s last update. This is an ISO 8601
    /// timestamp;
    /// *note that this is returned as a string*.
    update_time: ?i64 = null,

    pub const json_field_names = .{
        .arn = "arn",
        .create_time = "createTime",
        .destination_configuration = "destinationConfiguration",
        .id = "id",
        .name = "name",
        .state = "state",
        .tags = "tags",
        .update_time = "updateTime",
    };
};
