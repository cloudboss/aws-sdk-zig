const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;

const list_event_types = @import("list_event_types.zig");
const list_notification_rules = @import("list_notification_rules.zig");
const list_targets = @import("list_targets.zig");

pub const ListEventTypesPaginator = struct {
    client: *Client,
    params: list_event_types.ListEventTypesInput,
    next_token: ?[]const u8 = null,
    done: bool = false,
    allocator: std.mem.Allocator,

    const Self = @This();

    pub fn next(self: *Self, allocator: std.mem.Allocator, options: list_event_types.Options) !list_event_types.ListEventTypesOutput {
        if (self.done) {
            return error.EndOfPagination;
        }

        self.params.next_token = self.next_token;

        const output = try list_event_types.execute(self.client, allocator, self.params, options);

        if (output.next_token) |token| {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = self.allocator.dupe(u8, token) catch null;
        } else {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = null;
            self.done = true;
        }

        return output;
    }

    pub fn deinit(self: *Self) void {
        if (self.next_token) |token| {
            self.allocator.free(token);
        }
    }
};

