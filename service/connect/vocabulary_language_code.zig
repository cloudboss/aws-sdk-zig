    pub fn wireName(self: @This()) []const u8 {
        return switch (self) {
            .ar_ae => "ar-AE",
            .de_ch => "de-CH",
            .de_de => "de-DE",
            .en_ab => "en-AB",
            .en_au => "en-AU",
            .en_gb => "en-GB",
            .en_ie => "en-IE",
            .en_in => "en-IN",
            .en_us => "en-US",
            .en_wl => "en-WL",
            .es_es => "es-ES",
            .es_us => "es-US",
            .fr_ca => "fr-CA",
            .fr_fr => "fr-FR",
            .hi_in => "hi-IN",
            .it_it => "it-IT",
            .ja_jp => "ja-JP",
            .ko_kr => "ko-KR",
            .pt_br => "pt-BR",
            .pt_pt => "pt-PT",
            .zh_cn => "zh-CN",
            .en_nz => "en-NZ",
