        .en_in = "en-IN",
        .en_us = "en-US",
        .en_wl = "en-WL",
        .es_es = "es-ES",
        .es_us = "es-US",
        .fr_ca = "fr-CA",
        .fr_fr = "fr-FR",
        .hi_in = "hi-IN",
        .it_it = "it-IT",
        .ja_jp = "ja-JP",
        .ko_kr = "ko-KR",
        .pt_br = "pt-BR",
        .pt_pt = "pt-PT",
        .zh_cn = "zh-CN",
        .en_nz = "en-NZ",
        .en_za = "en-ZA",
        .ca_es = "ca-ES",
