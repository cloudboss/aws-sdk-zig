const aws = @import("aws");

const Certificate = @import("certificate.zig").Certificate;
const ConfigurationOverrides = @import("configuration_overrides.zig").ConfigurationOverrides;
const FailureReason = @import("failure_reason.zig").FailureReason;
const EndpointState = @import("endpoint_state.zig").EndpointState;

/// This entity represents the endpoint that is managed by Amazon EMR on EKS.
pub const Endpoint = struct {
    /// The ARN of the endpoint.
    arn: ?[]const u8 = null,

    /// The certificate ARN of the endpoint. This field is under deprecation and
    /// will be removed
    /// in future.
    certificate_arn: ?[]const u8 = null,

    /// The certificate generated by emr control plane on customer behalf to secure
    /// the managed
    /// endpoint.
    certificate_authority: ?Certificate = null,

    /// The configuration settings that are used to override existing configurations
    /// for
    /// endpoints.
    configuration_overrides: ?ConfigurationOverrides = null,

    /// The date and time when the endpoint was created.
    created_at: ?i64 = null,

    /// The execution role ARN of the endpoint.
    execution_role_arn: ?[]const u8 = null,

    /// The reasons why the endpoint has failed.
    failure_reason: ?FailureReason = null,

    /// The ID of the endpoint.
    id: ?[]const u8 = null,

    /// The name of the endpoint.
    name: ?[]const u8 = null,

    /// The EMR release version to be used for the endpoint.
    release_label: ?[]const u8 = null,

    /// The security group configuration of the endpoint.
    security_group: ?[]const u8 = null,

    /// The server URL of the endpoint.
    server_url: ?[]const u8 = null,

    /// The state of the endpoint.
    state: ?EndpointState = null,

    /// Additional details of the endpoint state.
    state_details: ?[]const u8 = null,

    /// The subnet IDs of the endpoint.
    subnet_ids: ?[]const []const u8 = null,

    /// The tags of the endpoint.
    tags: ?[]const aws.map.StringMapEntry = null,

    /// The type of the endpoint.
    @"type": ?[]const u8 = null,

    /// The ID of the endpoint's virtual cluster.
    virtual_cluster_id: ?[]const u8 = null,

    pub const json_field_names = .{
        .arn = "arn",
        .certificate_arn = "certificateArn",
        .certificate_authority = "certificateAuthority",
        .configuration_overrides = "configurationOverrides",
        .created_at = "createdAt",
        .execution_role_arn = "executionRoleArn",
        .failure_reason = "failureReason",
        .id = "id",
        .name = "name",
        .release_label = "releaseLabel",
        .security_group = "securityGroup",
        .server_url = "serverUrl",
        .state = "state",
        .state_details = "stateDetails",
        .subnet_ids = "subnetIds",
        .tags = "tags",
        .@"type" = "type",
        .virtual_cluster_id = "virtualClusterId",
    };
};
