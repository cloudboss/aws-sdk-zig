const ModelVersionStatus = @import("model_version_status.zig").ModelVersionStatus;
const ModelDiagnosticsOutputConfiguration = @import("model_diagnostics_output_configuration.zig").ModelDiagnosticsOutputConfiguration;
const ModelQuality = @import("model_quality.zig").ModelQuality;
const RetrainingSchedulerStatus = @import("retraining_scheduler_status.zig").RetrainingSchedulerStatus;
const ModelStatus = @import("model_status.zig").ModelStatus;

/// Provides information about the specified machine learning model, including
/// dataset and
/// model names and ARNs, as well as status.
pub const ModelSummary = struct {
    /// The model version that the inference scheduler uses to run an inference
    /// execution.
    active_model_version: ?i64,

    /// The Amazon Resource Name (ARN) of the model version that is set as active.
    /// The active
    /// model version is the model version that the inference scheduler uses to run
    /// an inference
    /// execution.
    active_model_version_arn: ?[]const u8,

    /// The time at which the specific model was created.
    created_at: ?i64,

    /// The Amazon Resource Name (ARN) of the dataset used to create the model.
    dataset_arn: ?[]const u8,

    /// The name of the dataset being used for the machine learning model.
    dataset_name: ?[]const u8,

    /// Indicates the most recent model version that was generated by retraining.
    latest_scheduled_retraining_model_version: ?i64,

    /// Indicates the start time of the most recent scheduled retraining run.
    latest_scheduled_retraining_start_time: ?i64,

    /// Indicates the status of the most recent scheduled retraining run.
    latest_scheduled_retraining_status: ?ModelVersionStatus,

    /// The Amazon Resource Name (ARN) of the machine learning model.
    model_arn: ?[]const u8,

    model_diagnostics_output_configuration: ?ModelDiagnosticsOutputConfiguration,

    /// The name of the machine learning model.
    model_name: ?[]const u8,

    /// Provides a quality assessment for a model that uses labels. If Lookout for
    /// Equipment determines that the
    /// model quality is poor based on training metrics, the value is
    /// `POOR_QUALITY_DETECTED`. Otherwise, the value is
    /// `QUALITY_THRESHOLD_MET`.
    ///
    /// If the model is unlabeled, the model quality can't
    /// be assessed and the value of `ModelQuality` is
    /// `CANNOT_DETERMINE_QUALITY`. In this situation, you can get a model quality
    /// assessment by adding labels to the input dataset and retraining the model.
    ///
    /// For information about using labels with your models, see [Understanding
    /// labeling](https://docs.aws.amazon.com/lookout-for-equipment/latest/ug/understanding-labeling.html).
    ///
    /// For information about improving the quality of a model, see [Best practices
    /// with
    /// Amazon Lookout for
    /// Equipment](https://docs.aws.amazon.com/lookout-for-equipment/latest/ug/best-practices.html).
    model_quality: ?ModelQuality,

    /// Indicates the date that the next scheduled retraining run will start on.
    /// Lookout for Equipment truncates
    /// the time you provide to [the nearest UTC
    /// day](https://docs.aws.amazon.com/https:/docs.aws.amazon.com/cli/latest/userguide/cli-usage-parameters-types.html#parameter-type-timestamp).
    next_scheduled_retraining_start_date: ?i64,

    /// Indicates the status of the retraining scheduler.
    retraining_scheduler_status: ?RetrainingSchedulerStatus,

    /// Indicates the status of the machine learning model.
    status: ?ModelStatus,

    pub const json_field_names = .{
        .active_model_version = "ActiveModelVersion",
        .active_model_version_arn = "ActiveModelVersionArn",
        .created_at = "CreatedAt",
        .dataset_arn = "DatasetArn",
        .dataset_name = "DatasetName",
        .latest_scheduled_retraining_model_version = "LatestScheduledRetrainingModelVersion",
        .latest_scheduled_retraining_start_time = "LatestScheduledRetrainingStartTime",
        .latest_scheduled_retraining_status = "LatestScheduledRetrainingStatus",
        .model_arn = "ModelArn",
        .model_diagnostics_output_configuration = "ModelDiagnosticsOutputConfiguration",
        .model_name = "ModelName",
        .model_quality = "ModelQuality",
        .next_scheduled_retraining_start_date = "NextScheduledRetrainingStartDate",
        .retraining_scheduler_status = "RetrainingSchedulerStatus",
        .status = "Status",
    };
};
