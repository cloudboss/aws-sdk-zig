    try body_buf.appendSlice(alloc, "{");

    if (has_prev) try body_buf.appendSlice(alloc, ",");
    try body_buf.appendSlice(alloc, "\"identityProviderConfig\":");
    try aws.json.writeValue(@TypeOf(input.identity_provider_config), input.identity_provider_config, alloc, &body_buf);
    has_prev = true;

