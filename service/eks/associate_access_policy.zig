    if (body.len > 0) {
        result = try aws.json.parseJsonObject(AssociateAccessPolicyOutput, body, allocator);
    }
    _ = status;
    _ = headers;

    return result;
}

fn parseErrorResponse(allocator: std.mem.Allocator, body: []const u8, status: u16) !ServiceError {
