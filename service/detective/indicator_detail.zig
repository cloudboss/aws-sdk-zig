const ImpossibleTravelDetail = @import("impossible_travel_detail.zig").ImpossibleTravelDetail;
const NewAsoDetail = @import("new_aso_detail.zig").NewAsoDetail;
const NewGeolocationDetail = @import("new_geolocation_detail.zig").NewGeolocationDetail;
const NewUserAgentDetail = @import("new_user_agent_detail.zig").NewUserAgentDetail;
