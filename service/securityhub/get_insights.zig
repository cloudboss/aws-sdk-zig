pub fn execute(client: *Client, allocator: std.mem.Allocator, input: GetInsightsInput, options: CallOptions) !GetInsightsOutput {
    var arena = std.heap.ArenaAllocator.init(client.allocator);
    defer arena.deinit();
    const alloc = arena.allocator();

