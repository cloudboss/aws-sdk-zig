const RestoreJobCreator = @import("restore_job_creator.zig").RestoreJobCreator;
const RestoreDeletionStatus = @import("restore_deletion_status.zig").RestoreDeletionStatus;
const RestoreJobStatus = @import("restore_job_status.zig").RestoreJobStatus;
const RestoreValidationStatus = @import("restore_validation_status.zig").RestoreValidationStatus;

/// Contains metadata about a restore job.
pub const RestoreJobsListMember = struct {
    /// The account ID that owns the restore job.
    account_id: ?[]const u8,

    /// The size, in bytes, of the restored resource.
    backup_size_in_bytes: ?i64,

    /// The Amazon Resource Name (ARN) of the backup vault containing the recovery
    /// point being restored. This helps identify vault access policies and
    /// permissions.
    backup_vault_arn: ?[]const u8,

    /// The date and time a job to restore a recovery point is completed, in Unix
    /// format and
    /// Coordinated Universal Time (UTC). The value of `CompletionDate` is accurate
    /// to
    /// milliseconds. For example, the value 1516925490.087 represents Friday,
    /// January 26, 2018
    /// 12:11:30.087 AM.
    completion_date: ?i64,

    /// Contains identifying information about the creation
    /// of a restore job.
    created_by: ?RestoreJobCreator,

    /// An Amazon Resource Name (ARN) that uniquely identifies a resource. The
    /// format of the ARN
    /// depends on the resource type.
    created_resource_arn: ?[]const u8,

    /// The date and time a restore job is created, in Unix format and Coordinated
    /// Universal
    /// Time (UTC). The value of `CreationDate` is accurate to milliseconds. For
    /// example, the value 1516925490.087 represents Friday, January 26, 2018
    /// 12:11:30.087
    /// AM.
    creation_date: ?i64,

    /// This notes the status of the data generated by the restore
    /// test. The status may be `Deleting`, `Failed`,
    /// or `Successful`.
    deletion_status: ?RestoreDeletionStatus,

    /// This describes the restore job deletion status.
    deletion_status_message: ?[]const u8,

    /// The amount of time in minutes that a job restoring a recovery point is
    /// expected to
    /// take.
    expected_completion_time_minutes: ?i64,

    /// The IAM role ARN used to create the target recovery point; for example,
    /// `arn:aws:iam::123456789012:role/S3Access`.
    iam_role_arn: ?[]const u8,

    /// This is a boolean value indicating whether the restore job is a parent
    /// (composite) restore job.
    is_parent: bool = false,

    /// This is the unique identifier of the parent restore job for the selected
    /// restore job.
    parent_job_id: ?[]const u8,

    /// Contains an estimated percentage complete of a job at the time the job
    /// status was
    /// queried.
    percent_done: ?[]const u8,

    /// An ARN that uniquely identifies a recovery point; for example,
    /// `arn:aws:backup:us-east-1:123456789012:recovery-point:1EB3B5E7-9EB0-435A-A80B-108B488B0D45`.
    recovery_point_arn: ?[]const u8,

    /// The date on which a recovery point was created.
    recovery_point_creation_date: ?i64,

    /// The resource type of the listed restore jobs; for example, an Amazon Elastic
    /// Block Store
    /// (Amazon EBS) volume or an Amazon Relational Database Service (Amazon RDS)
    /// database.
    /// For Windows Volume Shadow Copy Service (VSS) backups, the only supported
    /// resource type is
    /// Amazon EC2.
    resource_type: ?[]const u8,

    /// Uniquely identifies the job that restores a recovery point.
    restore_job_id: ?[]const u8,

    /// The Amazon Resource Name (ARN) of the original resource that was backed up.
    /// This provides context about what resource is being restored.
    source_resource_arn: ?[]const u8,

    /// A status code specifying the state of the job initiated by Backup to restore
    /// a recovery point.
    status: ?RestoreJobStatus,

    /// A detailed message explaining the status of the job to restore a recovery
    /// point.
    status_message: ?[]const u8,

    /// The status of validation run on the indicated
    /// restore job.
    validation_status: ?RestoreValidationStatus,

    /// This describes the status of validation run on the
    /// indicated restore job.
    validation_status_message: ?[]const u8,

    pub const json_field_names = .{
        .account_id = "AccountId",
        .backup_size_in_bytes = "BackupSizeInBytes",
        .backup_vault_arn = "BackupVaultArn",
        .completion_date = "CompletionDate",
        .created_by = "CreatedBy",
        .created_resource_arn = "CreatedResourceArn",
        .creation_date = "CreationDate",
        .deletion_status = "DeletionStatus",
        .deletion_status_message = "DeletionStatusMessage",
        .expected_completion_time_minutes = "ExpectedCompletionTimeMinutes",
        .iam_role_arn = "IamRoleArn",
        .is_parent = "IsParent",
        .parent_job_id = "ParentJobId",
        .percent_done = "PercentDone",
        .recovery_point_arn = "RecoveryPointArn",
        .recovery_point_creation_date = "RecoveryPointCreationDate",
        .resource_type = "ResourceType",
        .restore_job_id = "RestoreJobId",
        .source_resource_arn = "SourceResourceArn",
        .status = "Status",
        .status_message = "StatusMessage",
        .validation_status = "ValidationStatus",
        .validation_status_message = "ValidationStatusMessage",
    };
};
