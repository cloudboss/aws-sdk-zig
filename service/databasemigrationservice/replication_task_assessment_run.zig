const ReplicationTaskAssessmentRunProgress = @import("replication_task_assessment_run_progress.zig").ReplicationTaskAssessmentRunProgress;
const ReplicationTaskAssessmentRunResultStatistic = @import("replication_task_assessment_run_result_statistic.zig").ReplicationTaskAssessmentRunResultStatistic;

/// Provides information that describes a premigration assessment run that you
/// have started
/// using the `StartReplicationTaskAssessmentRun` operation.
///
/// Some of the information appears based on other operations that can return
/// the
/// `ReplicationTaskAssessmentRun` object.
pub const ReplicationTaskAssessmentRun = struct {
    /// Indication of the completion progress for the individual assessments
    /// specified to
    /// run.
    assessment_progress: ?ReplicationTaskAssessmentRunProgress = null,

    /// Unique name of the assessment run.
    assessment_run_name: ?[]const u8 = null,

    /// Indicates that the following PreflightAssessmentRun is the latest for the
    /// ReplicationTask. The status is either true or false.
    is_latest_task_assessment_run: bool = false,

    /// Last message generated by an individual assessment failure.
    last_failure_message: ?[]const u8 = null,

    /// ARN of the migration task associated with this premigration assessment run.
    replication_task_arn: ?[]const u8 = null,

    /// Amazon Resource Name (ARN) of this assessment run.
    replication_task_assessment_run_arn: ?[]const u8 = null,

    /// Date on which the assessment run was created using the
    /// `StartReplicationTaskAssessmentRun` operation.
    replication_task_assessment_run_creation_date: ?i64 = null,

    /// Encryption mode used to encrypt the assessment run results.
    result_encryption_mode: ?[]const u8 = null,

    /// ARN of the KMS encryption key used to encrypt the assessment run results.
    result_kms_key_arn: ?[]const u8 = null,

    /// Amazon S3 bucket where DMS stores the results of this assessment run.
    result_location_bucket: ?[]const u8 = null,

    /// Folder in an Amazon S3 bucket where DMS stores the results of this
    /// assessment
    /// run.
    result_location_folder: ?[]const u8 = null,

    /// Result statistics for a completed assessment run, showing aggregated
    /// statistics of IndividualAssessments for how many assessments were passed,
    /// failed, or encountered issues such as errors or warnings.
    result_statistic: ?ReplicationTaskAssessmentRunResultStatistic = null,

    /// ARN of the service role used to start the assessment run using the
    /// `StartReplicationTaskAssessmentRun` operation. The role must allow the
    /// `iam:PassRole` action.
    service_access_role_arn: ?[]const u8 = null,

    /// Assessment run status.
    ///
    /// This status can have one of the following values:
    ///
    /// * `"cancelling"` – The assessment run was canceled by the
    /// `CancelReplicationTaskAssessmentRun` operation.
    ///
    /// * `"deleting"` – The assessment run was deleted by the
    /// `DeleteReplicationTaskAssessmentRun` operation.
    ///
    /// * `"failed"` – At least one individual assessment completed with a
    /// `failed` status.
    ///
    /// * `"error-provisioning"` – An internal error occurred while
    /// resources were provisioned (during `provisioning` status).
    ///
    /// * `"error-executing"` – An internal error occurred while
    /// individual assessments ran (during `running` status).
    ///
    /// * `"invalid state"` – The assessment run is in an unknown
    /// state.
    ///
    /// * `"passed"` – All individual assessments have completed, and none
    /// has a `failed` status.
    ///
    /// * `"provisioning"` – Resources required to run individual
    /// assessments are being provisioned.
    ///
    /// * `"running"` – Individual assessments are being run.
    ///
    /// * `"starting"` – The assessment run is starting, but resources are
    /// not yet being provisioned for individual assessments.
    ///
    /// * `"warning"` – At least one individual assessment completed with a
    ///   `warning` status or all individual assessments were skipped (completed
    ///   with a `skipped` status).
    status: ?[]const u8 = null,

    pub const json_field_names = .{
        .assessment_progress = "AssessmentProgress",
        .assessment_run_name = "AssessmentRunName",
        .is_latest_task_assessment_run = "IsLatestTaskAssessmentRun",
        .last_failure_message = "LastFailureMessage",
        .replication_task_arn = "ReplicationTaskArn",
        .replication_task_assessment_run_arn = "ReplicationTaskAssessmentRunArn",
        .replication_task_assessment_run_creation_date = "ReplicationTaskAssessmentRunCreationDate",
        .result_encryption_mode = "ResultEncryptionMode",
        .result_kms_key_arn = "ResultKmsKeyArn",
        .result_location_bucket = "ResultLocationBucket",
        .result_location_folder = "ResultLocationFolder",
        .result_statistic = "ResultStatistic",
        .service_access_role_arn = "ServiceAccessRoleArn",
        .status = "Status",
    };
};
