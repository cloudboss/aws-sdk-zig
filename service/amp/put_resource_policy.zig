pub fn execute(client: *Client, allocator: std.mem.Allocator, input: PutResourcePolicyInput, options: Options) !PutResourcePolicyOutput {
    var arena = std.heap.ArenaAllocator.init(client.allocator);
    defer arena.deinit();
