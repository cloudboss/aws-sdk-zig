const aws = @import("aws");
const std = @import("std");

const batch_get_findings = @import("batch_get_findings.zig");
const create_scan = @import("create_scan.zig");
const create_upload_url = @import("create_upload_url.zig");
const get_account_configuration = @import("get_account_configuration.zig");
const get_findings = @import("get_findings.zig");
const get_metrics_summary = @import("get_metrics_summary.zig");
const get_scan = @import("get_scan.zig");
const list_findings_metrics = @import("list_findings_metrics.zig");
const list_scans = @import("list_scans.zig");
const list_tags_for_resource = @import("list_tags_for_resource.zig");
const tag_resource = @import("tag_resource.zig");
const untag_resource = @import("untag_resource.zig");
const update_account_configuration = @import("update_account_configuration.zig");
const paginator = @import("paginator.zig");

pub const Client = struct {
    allocator: std.mem.Allocator,
    config: *aws.Config,
    http_client: aws.http.HttpClient,

    const Self = @This();
    pub const sdk_id = "CodeGuru Security";

    pub fn init(allocator: std.mem.Allocator, config: *aws.Config) Self {
        return .{
            .allocator = allocator,
            .config = config,
            .http_client = aws.http.HttpClient.init(allocator),
        };
    }

    pub fn initWithOptions(allocator: std.mem.Allocator, config: *aws.Config, options: aws.http.RequestOptions) Self {
        return .{
            .allocator = allocator,
            .config = config,
            .http_client = aws.http.HttpClient.initWithOptions(allocator, options),
        };
    }

    pub fn deinit(self: *Self) void {
        self.http_client.deinit();
    }

    /// Returns a list of requested findings from standard scans.
    pub fn batchGetFindings(self: *Self, allocator: std.mem.Allocator, input: batch_get_findings.BatchGetFindingsInput, options: batch_get_findings.Options) !batch_get_findings.BatchGetFindingsOutput {
        return batch_get_findings.execute(self, allocator, input, options);
    }

    /// Use to create a scan using code uploaded to an Amazon S3 bucket.
    pub fn createScan(self: *Self, allocator: std.mem.Allocator, input: create_scan.CreateScanInput, options: create_scan.Options) !create_scan.CreateScanOutput {
        return create_scan.execute(self, allocator, input, options);
    }

    /// Generates a pre-signed URL, request headers used to upload a code resource,
    /// and code artifact identifier for the uploaded resource.
    ///
    /// You can upload your code resource to the URL with the request headers using
    /// any HTTP client.
    pub fn createUploadUrl(self: *Self, allocator: std.mem.Allocator, input: create_upload_url.CreateUploadUrlInput, options: create_upload_url.Options) !create_upload_url.CreateUploadUrlOutput {
        return create_upload_url.execute(self, allocator, input, options);
    }

    /// Use to get the encryption configuration for an account.
    pub fn getAccountConfiguration(self: *Self, allocator: std.mem.Allocator, input: get_account_configuration.GetAccountConfigurationInput, options: get_account_configuration.Options) !get_account_configuration.GetAccountConfigurationOutput {
        return get_account_configuration.execute(self, allocator, input, options);
    }

    /// Returns a list of all findings generated by a particular scan.
    pub fn getFindings(self: *Self, allocator: std.mem.Allocator, input: get_findings.GetFindingsInput, options: get_findings.Options) !get_findings.GetFindingsOutput {
        return get_findings.execute(self, allocator, input, options);
    }

    /// Returns a summary of metrics for an account from a specified date, including
    /// number of open findings, the categories with most findings, the scans with
    /// most open findings, and scans with most open critical findings.
    pub fn getMetricsSummary(self: *Self, allocator: std.mem.Allocator, input: get_metrics_summary.GetMetricsSummaryInput, options: get_metrics_summary.Options) !get_metrics_summary.GetMetricsSummaryOutput {
        return get_metrics_summary.execute(self, allocator, input, options);
    }

    /// Returns details about a scan, including whether or not a scan has completed.
    pub fn getScan(self: *Self, allocator: std.mem.Allocator, input: get_scan.GetScanInput, options: get_scan.Options) !get_scan.GetScanOutput {
        return get_scan.execute(self, allocator, input, options);
    }

    /// Returns metrics about all findings in an account within a specified time
    /// range.
    pub fn listFindingsMetrics(self: *Self, allocator: std.mem.Allocator, input: list_findings_metrics.ListFindingsMetricsInput, options: list_findings_metrics.Options) !list_findings_metrics.ListFindingsMetricsOutput {
        return list_findings_metrics.execute(self, allocator, input, options);
    }

    /// Returns a list of all scans in an account. Does not return `EXPRESS` scans.
    pub fn listScans(self: *Self, allocator: std.mem.Allocator, input: list_scans.ListScansInput, options: list_scans.Options) !list_scans.ListScansOutput {
        return list_scans.execute(self, allocator, input, options);
    }

    /// Returns a list of all tags associated with a scan.
    pub fn listTagsForResource(self: *Self, allocator: std.mem.Allocator, input: list_tags_for_resource.ListTagsForResourceInput, options: list_tags_for_resource.Options) !list_tags_for_resource.ListTagsForResourceOutput {
        return list_tags_for_resource.execute(self, allocator, input, options);
    }

    /// Use to add one or more tags to an existing scan.
    pub fn tagResource(self: *Self, allocator: std.mem.Allocator, input: tag_resource.TagResourceInput, options: tag_resource.Options) !tag_resource.TagResourceOutput {
        return tag_resource.execute(self, allocator, input, options);
    }

    /// Use to remove one or more tags from an existing scan.
    pub fn untagResource(self: *Self, allocator: std.mem.Allocator, input: untag_resource.UntagResourceInput, options: untag_resource.Options) !untag_resource.UntagResourceOutput {
        return untag_resource.execute(self, allocator, input, options);
    }

    /// Use to update the encryption configuration for an account.
    pub fn updateAccountConfiguration(self: *Self, allocator: std.mem.Allocator, input: update_account_configuration.UpdateAccountConfigurationInput, options: update_account_configuration.Options) !update_account_configuration.UpdateAccountConfigurationOutput {
        return update_account_configuration.execute(self, allocator, input, options);
    }

    pub fn getFindingsPaginator(self: *Self, params: get_findings.GetFindingsInput) paginator.GetFindingsPaginator {
        return .{
            .client = self,
            .params = params,
            .allocator = self.allocator,
        };
    }

    pub fn listFindingsMetricsPaginator(self: *Self, params: list_findings_metrics.ListFindingsMetricsInput) paginator.ListFindingsMetricsPaginator {
        return .{
            .client = self,
            .params = params,
            .allocator = self.allocator,
        };
    }

    pub fn listScansPaginator(self: *Self, params: list_scans.ListScansInput) paginator.ListScansPaginator {
        return .{
            .client = self,
            .params = params,
            .allocator = self.allocator,
        };
    }
};
