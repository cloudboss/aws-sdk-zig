/// **Note:**
///
/// For asynchronous function invocation, use Invoke.
///
/// Invokes a function asynchronously.
///
/// **Note:**
///
/// The payload limit is 256KB. For larger payloads, for up to 1MB, use Invoke.
///
/// **Note:**
///
/// If you do use the InvokeAsync action, note that it doesn't support the use
/// of X-Ray active tracing. Trace ID is not propagated to the function, even if
/// X-Ray active tracing is turned on.
pub const InvokeAsyncInput = struct {
    /// The name or ARN of the Lambda function. **Name formats**
    ///
    /// * **Function name** – `my-function`.
    /// * **Function ARN** –
    ///   `arn:aws:lambda:us-west-2:123456789012:function:my-function`.
    /// * **Partial ARN** – `123456789012:function:my-function`.
    ///
    /// The length constraint applies only to the full ARN. If you specify only the
    /// function name, it is limited to 64 characters in length.
    function_name: []const u8,

    /// The JSON that you want to provide to your Lambda function as input.
    invoke_args: []const u8,

    pub const json_field_names = .{
        .function_name = "FunctionName",
        .invoke_args = "InvokeArgs",
    };
};

pub const InvokeAsyncOutput = struct {
    /// The status code.
    status: ?i32 = null,

    _arena: std.heap.ArenaAllocator = undefined,

    pub fn deinit(self: *InvokeAsyncOutput) void {
        self._arena.deinit();
    }

    pub const json_field_names = .{
        .status = "Status",
    };
};

pub const Options = struct {
    diagnostic: ?*ServiceError = null,
};

pub fn execute(client: *Client, input: InvokeAsyncInput, options: Options) !InvokeAsyncOutput {
    var arena = std.heap.ArenaAllocator.init(client.allocator);
    defer arena.deinit();
    const alloc = arena.allocator();

    var request = try serializeRequest(alloc, input, client.config);
    defer request.deinit(alloc);

    const creds = try client.config.credentials.getCredentials(alloc);
    try aws.signing.signRequest(alloc, &request, creds, client.config.region, "lambda");

    var response = try client.http_client.sendRequest(&request);
    defer response.deinit();

    if (!response.isSuccess()) {
        if (options.diagnostic) |d| {
            d.* = parseErrorResponse(response.body, response.status);
        }
        return error.ServiceError;
    }

    var resp_arena = std.heap.ArenaAllocator.init(client.allocator);
    errdefer resp_arena.deinit();
    var result = try deserializeResponse(response.body, response.status, response.headers, resp_arena.allocator());
    result._arena = resp_arena;
    return result;
}

fn serializeRequest(alloc: std.mem.Allocator, input: InvokeAsyncInput, config: *aws.Config) !aws.http.Request {
    const endpoint = try config.getEndpoint("lambda", alloc);

    const host = aws.url.parseHost(endpoint);
    const tls = !std.mem.startsWith(u8, endpoint, "http://");
    const port = aws.url.parsePort(endpoint);

    var path_buf: std.ArrayList(u8) = .{};
    try path_buf.appendSlice(alloc, "/2014-11-13/functions/");
    try path_buf.appendSlice(alloc, input.function_name);
    try path_buf.appendSlice(alloc, "/invoke-async");
    const path = try path_buf.toOwnedSlice(alloc);

    const body = input.invoke_args;

    var request = aws.http.Request.init(host);
    request.method = .POST;
    request.path = path;
    request.tls = tls;
    request.port = port;
    request.body = body;
    try request.headers.put(alloc, "Content-Type", "application/json");

    return request;
}

fn deserializeResponse(body: []const u8, status: u16, headers: anytype, alloc: std.mem.Allocator) !InvokeAsyncOutput {
    _ = alloc;
    var result: InvokeAsyncOutput = .{};
    _ = body;
    result.status = @intCast(status);
    _ = headers;

    return result;
}

