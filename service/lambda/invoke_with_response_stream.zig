/// Configure your Lambda functions to stream response payloads back to clients.
/// For more information, see [Configuring a Lambda function to stream
/// responses](https://docs.aws.amazon.com/lambda/latest/dg/configuration-response-streaming.html).
///
/// This operation requires permission for the
/// [lambda:InvokeFunction](https://docs.aws.amazon.com/IAM/latest/UserGuide/list_awslambda.html) action. For details on how to set up permissions for cross-account invocations, see [Granting function access to other accounts](https://docs.aws.amazon.com/lambda/latest/dg/access-control-resource-based.html#permissions-resource-xaccountinvoke).
pub const InvokeWithResponseStreamInput = struct {
    /// Up to 3,583 bytes of base64-encoded data about the invoking client to pass
    /// to the function in the context object.
    client_context: ?[]const u8 = null,

    /// The name or ARN of the Lambda function. **Name formats**
    ///
    /// * **Function name** – `my-function`.
    /// * **Function ARN** –
    ///   `arn:aws:lambda:us-west-2:123456789012:function:my-function`.
    /// * **Partial ARN** – `123456789012:function:my-function`.
    ///
    /// The length constraint applies only to the full ARN. If you specify only the
    /// function name, it is limited to 64 characters in length.
    function_name: []const u8,

    /// Use one of the following options:
    ///
    /// * `RequestResponse` (default) – Invoke the function synchronously. Keep the
    ///   connection open until the function returns a response or times out. The
    ///   API operation response includes the function response and additional data.
    /// * `DryRun` – Validate parameter values and verify that the IAM user or role
    ///   has permission to invoke the function.
    invocation_type: ?ResponseStreamingInvocationType = null,

    /// Set to `Tail` to include the execution log in the response. Applies to
    /// synchronously invoked functions only.
    log_type: ?LogType = null,

    /// The JSON that you want to provide to your Lambda function as input.
    ///
    /// You can enter the JSON directly. For example, `--payload '{ "key": "value"
    /// }'`. You can also specify a file path. For example, `--payload
    /// file://payload.json`.
    payload: ?[]const u8 = null,

    /// The alias name.
    qualifier: ?[]const u8 = null,

    /// The identifier of the tenant in a multi-tenant Lambda function.
    tenant_id: ?[]const u8 = null,

    pub const json_field_names = .{
        .client_context = "ClientContext",
        .function_name = "FunctionName",
        .invocation_type = "InvocationType",
        .log_type = "LogType",
        .payload = "Payload",
        .qualifier = "Qualifier",
        .tenant_id = "TenantId",
    };
};

pub const InvokeWithResponseStreamOutput = struct {
    /// The stream of response payloads.
    event_stream: ?InvokeWithResponseStreamResponseEvent = null,

    /// The version of the function that executed. When you invoke a function with
    /// an alias, this indicates which version the alias resolved to.
    executed_version: ?[]const u8 = null,

    /// The type of data the stream is returning.
    response_stream_content_type: ?[]const u8 = null,

    /// For a successful request, the HTTP status code is in the 200 range. For the
    /// `RequestResponse` invocation type, this status code is 200. For the `DryRun`
    /// invocation type, this status code is 204.
    status_code: ?i32 = null,

    _arena: std.heap.ArenaAllocator = undefined,

    pub fn deinit(self: *InvokeWithResponseStreamOutput) void {
        self._arena.deinit();
    }

    pub const json_field_names = .{
        .event_stream = "EventStream",
        .executed_version = "ExecutedVersion",
        .response_stream_content_type = "ResponseStreamContentType",
        .status_code = "StatusCode",
    };
};

pub const Options = struct {
    diagnostic: ?*ServiceError = null,
};

pub fn execute(client: *Client, input: InvokeWithResponseStreamInput, options: Options) !InvokeWithResponseStreamOutput {
    var arena = std.heap.ArenaAllocator.init(client.allocator);
    defer arena.deinit();
    const alloc = arena.allocator();

    var request = try serializeRequest(alloc, input, client.config);
    defer request.deinit(alloc);

    const creds = try client.config.credentials.getCredentials(alloc);
    try aws.signing.signRequest(alloc, &request, creds, client.config.region, "lambda");

    var response = try client.http_client.sendRequest(&request);
    defer response.deinit();

    if (!response.isSuccess()) {
        if (options.diagnostic) |d| {
            d.* = parseErrorResponse(response.body, response.status);
        }
        return error.ServiceError;
    }

    var resp_arena = std.heap.ArenaAllocator.init(client.allocator);
    errdefer resp_arena.deinit();
    var result = try deserializeResponse(response.body, response.status, response.headers, resp_arena.allocator());
    result._arena = resp_arena;
    return result;
}

fn serializeRequest(alloc: std.mem.Allocator, input: InvokeWithResponseStreamInput, config: *aws.Config) !aws.http.Request {
    const endpoint = try config.getEndpoint("lambda", alloc);

    const host = parseHost(endpoint);
    const tls = !std.mem.startsWith(u8, endpoint, "http://");
    const port = parsePort(endpoint);

    var path_buf: std.ArrayList(u8) = .{};
    try path_buf.appendSlice(alloc, "/2021-11-15/functions/");
    try path_buf.appendSlice(alloc, input.function_name);
    try path_buf.appendSlice(alloc, "/response-streaming-invocations");
    const path = try path_buf.toOwnedSlice(alloc);

    var query_buf: std.ArrayList(u8) = .{};
    var query_has_prev = false;
    if (input.qualifier) |v| {
        if (query_has_prev) try query_buf.appendSlice(alloc, "&");
        try query_buf.appendSlice(alloc, "Qualifier=");
        try appendUrlEncoded(alloc, &query_buf, v);
        query_has_prev = true;
    }
    const query = try query_buf.toOwnedSlice(alloc);

    const body = input.payload orelse "";

    var request = aws.http.Request.init(host);
    request.method = .POST;
    request.path = path;
    request.tls = tls;
    request.port = port;
    request.body = body;
    request.query = query;
    try request.headers.put(alloc, "Content-Type", "application/json");
    if (input.client_context) |v| {
        try request.headers.put(alloc, "X-Amz-Client-Context", v);
    }
    if (input.invocation_type) |v| {
        try request.headers.put(alloc, "X-Amz-Invocation-Type", @tagName(v));
    }
    if (input.log_type) |v| {
        try request.headers.put(alloc, "X-Amz-Log-Type", @tagName(v));
    }
    if (input.tenant_id) |v| {
        try request.headers.put(alloc, "X-Amz-Tenant-Id", v);
    }

    return request;
}

fn deserializeResponse(body: []const u8, status: u16, headers: anytype, alloc: std.mem.Allocator) !InvokeWithResponseStreamOutput {
    var result: InvokeWithResponseStreamOutput = .{};
    _ = body;
    result.status_code = @intCast(status);
    if (headers.get("x-amz-executed-version")) |value| {
        result.executed_version = try alloc.dupe(u8, value);
    }
    if (headers.get("content-type")) |value| {
        result.response_stream_content_type = try alloc.dupe(u8, value);
    }

    return result;
}

