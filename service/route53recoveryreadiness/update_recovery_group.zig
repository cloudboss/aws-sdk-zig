    var path_buf: std.ArrayList(u8) = .{};
    try path_buf.appendSlice(allocator, "/recoverygroups/");
    try path_buf.appendSlice(allocator, input.recovery_group_name);
    const path = try path_buf.toOwnedSlice(allocator);

