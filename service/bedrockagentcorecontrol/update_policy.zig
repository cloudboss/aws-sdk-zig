    var result: UpdatePolicyOutput = .{};
    if (body.len > 0) {
        result = try aws.json.parseJsonObject(UpdatePolicyOutput, body, allocator);
    }
    _ = status;
    _ = headers;

    return result;
}

fn parseErrorResponse(allocator: std.mem.Allocator, body: []const u8, status: u16) !ServiceError {
