    try body_buf.appendSlice(alloc, "\"aggregateStatus\":");
    try aws.json.writeValue(@TypeOf(input.aggregate_status), input.aggregate_status, alloc, &body_buf);
    has_prev = true;
    if (has_prev) try body_buf.appendSlice(alloc, ",");
    try body_buf.appendSlice(alloc, "\"componentStatuses\":");
    try aws.json.writeValue(@TypeOf(input.component_statuses), input.component_statuses, alloc, &body_buf);
    has_prev = true;
    if (has_prev) try body_buf.appendSlice(alloc, ",");
    try body_buf.appendSlice(alloc, "\"taskId\":");
    try aws.json.writeValue(@TypeOf(input.task_id), input.task_id, alloc, &body_buf);
