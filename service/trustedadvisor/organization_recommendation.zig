const RecommendationLifecycleStage = @import("recommendation_lifecycle_stage.zig").RecommendationLifecycleStage;
const RecommendationPillar = @import("recommendation_pillar.zig").RecommendationPillar;
const RecommendationPillarSpecificAggregates = @import("recommendation_pillar_specific_aggregates.zig").RecommendationPillarSpecificAggregates;
const RecommendationResourcesAggregates = @import("recommendation_resources_aggregates.zig").RecommendationResourcesAggregates;
const RecommendationSource = @import("recommendation_source.zig").RecommendationSource;
const RecommendationStatus = @import("recommendation_status.zig").RecommendationStatus;
const RecommendationType = @import("recommendation_type.zig").RecommendationType;
const UpdateRecommendationLifecycleStageReasonCode = @import("update_recommendation_lifecycle_stage_reason_code.zig").UpdateRecommendationLifecycleStageReasonCode;

/// A Recommendation for accounts within an Organization
pub const OrganizationRecommendation = struct {
    /// The ARN of the Recommendation
    arn: []const u8,

    /// The AWS Services that the Recommendation applies to
    aws_services: ?[]const []const u8 = null,

    /// The AWS Trusted Advisor Check ARN that relates to the Recommendation
    check_arn: ?[]const u8 = null,

    /// When the Recommendation was created, if created by AWS Trusted Advisor
    /// Priority
    created_at: ?i64 = null,

    /// The creator, if created by AWS Trusted Advisor Priority
    created_by: ?[]const u8 = null,

    /// A description for AWS Trusted Advisor recommendations
    description: []const u8,

    /// The ID which identifies where the Recommendation was produced
    id: []const u8,

    /// When the Recommendation was last updated
    last_updated_at: ?i64 = null,

    /// The lifecycle stage from AWS Trusted Advisor Priority
    lifecycle_stage: ?RecommendationLifecycleStage = null,

    /// The name of the AWS Trusted Advisor Recommendation
    name: []const u8,

    /// The Pillars that the Recommendation is optimizing
    pillars: []const RecommendationPillar,

    /// The pillar aggregations for cost savings
    pillar_specific_aggregates: ?RecommendationPillarSpecificAggregates = null,

    /// When the Recommendation was resolved
    resolved_at: ?i64 = null,

    /// An aggregation of all resources
    resources_aggregates: RecommendationResourcesAggregates,

    /// The source of the Recommendation
    source: RecommendationSource,

    /// The status of the Recommendation
    status: RecommendationStatus,

    /// Whether the Recommendation was automated or generated by AWS Trusted Advisor
    /// Priority
    @"type": RecommendationType,

    /// The person on whose behalf a Technical Account Manager (TAM) updated the
    /// recommendation. This information
    /// is only available when a Technical Account Manager takes an action on a
    /// recommendation managed by AWS
    /// Trusted Advisor Priority
    updated_on_behalf_of: ?[]const u8 = null,

    /// The job title of the person on whose behalf a Technical Account Manager
    /// (TAM) updated the recommendation.
    /// This information is only available when a Technical Account Manager takes an
    /// action on a recommendation
    /// managed by AWS Trusted Advisor Priority
    updated_on_behalf_of_job_title: ?[]const u8 = null,

    /// Reason for the lifecycle stage change
    update_reason: ?[]const u8 = null,

    /// Reason code for the lifecycle state change
    update_reason_code: ?UpdateRecommendationLifecycleStageReasonCode = null,

    pub const json_field_names = .{
        .arn = "arn",
        .aws_services = "awsServices",
        .check_arn = "checkArn",
        .created_at = "createdAt",
        .created_by = "createdBy",
        .description = "description",
        .id = "id",
        .last_updated_at = "lastUpdatedAt",
        .lifecycle_stage = "lifecycleStage",
        .name = "name",
        .pillars = "pillars",
        .pillar_specific_aggregates = "pillarSpecificAggregates",
        .resolved_at = "resolvedAt",
        .resources_aggregates = "resourcesAggregates",
        .source = "source",
        .status = "status",
        .@"type" = "type",
        .updated_on_behalf_of = "updatedOnBehalfOf",
        .updated_on_behalf_of_job_title = "updatedOnBehalfOfJobTitle",
        .update_reason = "updateReason",
        .update_reason_code = "updateReasonCode",
    };
};
