const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;

const list_certificate_authorities = @import("list_certificate_authorities.zig");
const list_permissions = @import("list_permissions.zig");
const list_tags = @import("list_tags.zig");

pub const ListCertificateAuthoritiesPaginator = struct {
    client: *Client,
    params: list_certificate_authorities.ListCertificateAuthoritiesInput,
    next_token: ?[]const u8 = null,
    done: bool = false,
    allocator: std.mem.Allocator,

    const Self = @This();

    pub fn next(self: *Self, allocator: std.mem.Allocator, options: list_certificate_authorities.Options) !list_certificate_authorities.ListCertificateAuthoritiesOutput {
        if (self.done) {
            return error.EndOfPagination;
        }

        self.params.next_token = self.next_token;

        const output = try list_certificate_authorities.execute(self.client, allocator, self.params, options);

        if (output.next_token) |token| {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = self.allocator.dupe(u8, token) catch null;
        } else {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = null;
            self.done = true;
        }

        return output;
    }

    pub fn deinit(self: *Self) void {
        if (self.next_token) |token| {
            self.allocator.free(token);
        }
    }
};

