/// Represents the data structure of a method's request or response payload.
pub const Model = struct {
    /// The content-type for the model.
    content_type: ?[]const u8 = null,

    /// The description of the model.
    description: ?[]const u8 = null,

    /// The identifier for the model resource.
    id: ?[]const u8 = null,

    /// The name of the model. Must be an alphanumeric string.
    name: ?[]const u8 = null,

    /// The schema for the model. For `application/json` models, this should be JSON
    /// schema draft 4 model. Do not include "\*/" characters in the description of
    /// any properties because such "\*/" characters may be interpreted as the
    /// closing marker for comments in some languages, such as Java or JavaScript,
    /// causing the installation of your API's SDK generated by API Gateway to fail.
    schema: ?[]const u8 = null,

    pub const json_field_names = .{
        .content_type = "contentType",
        .description = "description",
        .id = "id",
        .name = "name",
        .schema = "schema",
    };
};
