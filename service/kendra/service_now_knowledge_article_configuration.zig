const DataSourceToIndexFieldMapping = @import("data_source_to_index_field_mapping.zig").DataSourceToIndexFieldMapping;

/// Provides the configuration information for crawling knowledge articles in
/// the
/// ServiceNow site.
pub const ServiceNowKnowledgeArticleConfiguration = struct {
    /// `TRUE` to index attachments to knowledge articles.
    crawl_attachments: bool = false,

    /// The name of the ServiceNow field that is mapped to the index document
    /// contents field
    /// in the Amazon Kendra index.
    document_data_field_name: []const u8,

    /// The name of the ServiceNow field that is mapped to the index document title
    /// field.
    document_title_field_name: ?[]const u8 = null,

    /// A list of regular expression patterns applied to exclude certain knowledge
    /// article
    /// attachments. Attachments that match the patterns are excluded from the
    /// index. Items that
    /// don't match the patterns are included in the index. If an item matches both
    /// an inclusion
    /// and exclusion pattern, the exclusion pattern takes precedence and the item
    /// isn't
    /// included in the index.
    exclude_attachment_file_patterns: ?[]const []const u8 = null,

    /// Maps attributes or field names of knoweldge articles to Amazon Kendra index
    /// field
    /// names. To create custom fields, use the `UpdateIndex` API before you map to
    /// ServiceNow fields. For more information, see [Mapping data source
    /// fields](https://docs.aws.amazon.com/kendra/latest/dg/field-mapping.html).
    /// The
    /// ServiceNow data source field names must exist in your ServiceNow custom
    /// metadata.
    field_mappings: ?[]const DataSourceToIndexFieldMapping = null,

    /// A query that selects the knowledge articles to index. The query can return
    /// articles
    /// from multiple knowledge bases, and the knowledge bases can be public or
    /// private.
    ///
    /// The query string must be one generated by the ServiceNow console. For more
    /// information, see [Specifying documents to index with a
    /// query](https://docs.aws.amazon.com/kendra/latest/dg/servicenow-query.html).
    filter_query: ?[]const u8 = null,

    /// A list of regular expression patterns applied to include knowledge article
    /// attachments. Attachments that match the patterns are included in the index.
    /// Items that
    /// don't match the patterns are excluded from the index. If an item matches
    /// both an
    /// inclusion and exclusion pattern, the exclusion pattern takes precedence and
    /// the item
    /// isn't included in the index.
    include_attachment_file_patterns: ?[]const []const u8 = null,

    pub const json_field_names = .{
        .crawl_attachments = "CrawlAttachments",
        .document_data_field_name = "DocumentDataFieldName",
        .document_title_field_name = "DocumentTitleFieldName",
        .exclude_attachment_file_patterns = "ExcludeAttachmentFilePatterns",
        .field_mappings = "FieldMappings",
        .filter_query = "FilterQuery",
        .include_attachment_file_patterns = "IncludeAttachmentFilePatterns",
    };
};
