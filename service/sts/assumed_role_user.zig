/// The identifiers for the temporary security credentials that the operation
/// returns.
pub const AssumedRoleUser = struct {
    /// The ARN of the temporary security credentials that are returned from the
    /// AssumeRole action. For more information about ARNs and how to use them in
    /// policies, see [IAM
    /// Identifiers](https://docs.aws.amazon.com/IAM/latest/UserGuide/reference_identifiers.html) in the
    /// *IAM User Guide*.
    arn: []const u8,

    /// A unique identifier that contains the role ID and the role session name of
    /// the role that
    /// is being assumed. The role ID is generated by Amazon Web Services when the
    /// role is created.
    assumed_role_id: []const u8,
};
