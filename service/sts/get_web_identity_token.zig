fn serializeRequest(alloc: std.mem.Allocator, input: GetWebIdentityTokenInput, config: *aws.Config) !aws.http.Request {
    const endpoint = try config.getEndpoint("sts", alloc);

    const host = aws.url.parseHost(endpoint);
    const tls = !std.mem.startsWith(u8, endpoint, "http://");
    const port = aws.url.parsePort(endpoint);

    var body_buf: std.ArrayList(u8) = .{};

    try body_buf.appendSlice(alloc, "Action=GetWebIdentityToken&Version=2011-06-15");
    for (input.audience, 0..) |item, idx| {
        const n = idx + 1;
