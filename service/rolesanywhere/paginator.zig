const aws = @import("aws");
const std = @import("std");

const Client = @import("client.zig").Client;

const list_crls = @import("list_crls.zig");
const list_profiles = @import("list_profiles.zig");
const list_subjects = @import("list_subjects.zig");
const list_trust_anchors = @import("list_trust_anchors.zig");

pub const ListCrlsPaginator = struct {
    client: *Client,
    params: list_crls.ListCrlsInput,
    next_token: ?[]const u8 = null,
    done: bool = false,
    allocator: std.mem.Allocator,

    const Self = @This();

    pub fn next(self: *Self, allocator: std.mem.Allocator, options: list_crls.Options) !list_crls.ListCrlsOutput {
        if (self.done) {
            return error.EndOfPagination;
        }

        self.params.next_token = self.next_token;

        const output = try list_crls.execute(self.client, allocator, self.params, options);

        if (output.next_token) |token| {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = self.allocator.dupe(u8, token) catch null;
        } else {
            if (self.next_token) |old| {
                self.allocator.free(old);
            }
            self.next_token = null;
            self.done = true;
        }

        return output;
    }

    pub fn deinit(self: *Self) void {
        if (self.next_token) |token| {
            self.allocator.free(token);
        }
    }
};

