fn serializeRequest(alloc: std.mem.Allocator, input: CreateBlueprintInput, config: *aws.Config) !aws.http.Request {
    const endpoint = try config.getEndpointForService("bedrockdataautomation", "Bedrock Data Automation", alloc);

    const host = aws.url.parseHost(endpoint);
    const tls = !std.mem.startsWith(u8, endpoint, "http://");
    const port = aws.url.parsePort(endpoint);

    const path = "/blueprints/";

    var body_buf: std.ArrayList(u8) = .{};
