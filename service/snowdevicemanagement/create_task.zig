fn serializeRequest(alloc: std.mem.Allocator, input: CreateTaskInput, config: *aws.Config) !aws.http.Request {
    const endpoint = try config.getEndpointForService("snowdevicemanagement", "Snow Device Management", alloc);

    const host = aws.url.parseHost(endpoint);
    const tls = !std.mem.startsWith(u8, endpoint, "http://");
    const port = aws.url.parsePort(endpoint);

    const path = "/task";

    var body_buf: std.ArrayList(u8) = .{};
    var has_prev = false;
    try body_buf.appendSlice(alloc, "{");

